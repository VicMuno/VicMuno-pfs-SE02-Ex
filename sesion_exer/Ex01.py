# módulo (resto de la división)

a = 5
b = 7

print("La suma de a =", a, "mas b =", b, "da como resultado:", a + b)
print("La resta de b =", b, "menos a =", a, "da como resultado:", b - a)
print("La multiplicación de a =", a, "por b =", b, "da como resultado:", a * b)
print("La división de b =", b, "entre a =", a, "da como resultado:", b / a)
print("La división entera de b =", b, "entre a =", a, "da como resultado:", b // a)
print("el módulo de la división de b =", b, "entre a =", a, "da como resultado:", b % a)